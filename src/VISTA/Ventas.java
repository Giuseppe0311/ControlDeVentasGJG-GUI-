    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione por favor");
        }
        int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date fecha = jDateChooser1.getDate();
        Date fecha2 = jDateChooser2.getDate();
        String resultado1 = sdf.format(fecha);
        String resultado2 = sdf.format(fecha2);

        try {
            Date result = sdf.parse(resultado1);
            Date result1 = sdf.parse(resultado2);
            java.sql.Date sqldate1 = new java.sql.Date(result.getTime());
            java.sql.Date sqldate2 = new java.sql.Date(result1.getTime());
            Conectar Con = new Conectar();
            Connection conn = Con.conectar();
            PreparedStatement ps;
            String sql = "UPDATE ventas SET fecha_envio_supervisor=?, fecha_retorno_asesor=? WHERE idventa=?;";
            ps = conn.prepareStatement(sql);
            ps.setDate(1, sqldate1);
            ps.setDate(2, sqldate2);
            ps.setInt(3, id);
            ps.executeUpdate();
            llenartabla();
            JOptionPane.showMessageDialog(null, "Registro Actualizado");
            jDateChooser1.setEnabled(false);
            jDateChooser2.setEnabled(false);
            jTextArea1.setEditable(false);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        } catch (ParseException ex) {
            Logger.getLogger(Ventas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            PreparedStatement ps;
            ResultSet rs;
            int fila = jTable1.getSelectedRow();
            if (fila == -1) {
                jDateChooser1.setEnabled(false);
                jDateChooser2.setEnabled(false);
                jTextArea1.setEditable(false);
            }
            jDateChooser1.setEnabled(true);
            jDateChooser2.setEnabled(true);
            jTextArea1.setEditable(true);
            int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
            Conectar conn = new Conectar();
            Connection con = conn.conectar();
            String sql = "SELECT idventa,fecha_envio_supervisor,fecha_retorno_asesor,comentarios from ventas where idventa=?";
            ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while (rs.next()) {
                jDateChooser1.setDate(rs.getDate("fecha_envio_supervisor"));
                jDateChooser2.setDate(rs.getDate("fecha_retorno_asesor"));
                jTextArea1.setText(rs.getString("comentarios"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

