    public void llenartabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        Conectar conn = new Conectar();
        Connection con = conn.conectar();
        String sql = "SELECT idventa, nmr_contrato, TO_CHAR(fecha_contrato,'dd-MM-yyyy'), TO_CHAR(fecha_envio_supervisor,'dd-MM-yyyy'), TO_CHAR(fecha_retorno_asesor,'dd-MM-yyyy'), monto, clientes.nombre , planes.nombre_plan from ventas INNER JOIN CLIENTES ON CLIENTES.dni = ventas.id_cliente INNER JOIN planes ON planes.id_plan = ventas.id_plan";
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            Object[] obj = new Object[columnas];
            while (rs.next()) {
                for (int i = 0; i < columnas; i++) {
                    obj[i] = rs.getObject(i + 1);
                }

                modelo.addRow(obj);

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }

